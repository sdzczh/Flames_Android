public class GroupInfo {
    String qrUrl;
    String desc;
    String num;

    public void setQrUrl(String qrUrl) {
        this.qrUrl = qrUrl;
    }

    public String getQrUrl() {
        return qrUrl;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public String getDesc() {
        return desc;
    }

    public void setNum(String num) {
        this.num = num;
    }

    public String getNum() {
        return num;
    }
}
